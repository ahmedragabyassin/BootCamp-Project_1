#ifndef DATATYPES_H_
#define DATATYPES_H_


typedef unsigned char        uint8   ;
typedef unsigned short       uint16  ;
typedef unsigned long        uint32  ;
typedef unsigned long long   uint64  ;
typedef signed char          sint8   ;
typedef signed short         sint16  ;
typedef signed long          sint32  ;
typedef signed long long     sint64  ;
typedef unsigned char *      ptr8    ;
typedef unsigned short *     ptr16   ;
typedef unsigned long *      ptr32   ;
typedef unsigned long long * ptr64   ;
typedef signed char *        sptr8   ;
typedef signed short *       sptr16  ;
typedef signed long *        sptr32  ;
typedef signed long long *   sptr64  ;
typedef float                float32 ;
typedef double               float64 ;
typedef float*               fptr32  ;
typedef double*              fptr64  ;


#endif /* DATATYPES_H_ */