#ifndef TYPES_H_
#define TYPES_H_

#define NULL				'\0'




typedef unsigned char		 uint8  ;
typedef signed   char		 int8   ;
typedef unsigned short		 uint16 ;
typedef signed   short		 int16  ;
typedef unsigned long		 uint32 ;
typedef signed   long		 int32  ;
typedef unsigned long long   uint64 ;
typedef signed   long long   int64  ;

typedef unsigned char*		  uptr_8   ;
typedef signed   char*		  sptr_8   ;
typedef unsigned short*		  uptr_16  ;
typedef signed   short*		  sptr_16  ;
typedef unsigned long*		  uptr_32  ;
typedef signed   long*		  sptr_32  ;
typedef unsigned long long*   uptr_64  ;
typedef signed   long long*   sptr_64  ;







#endif /* TYPES_H_ */